package model;

import java.util.Date;

/**
 *
 * @author Inspiron
 */
public class User {
    private int id;
    private String pass;
    private String name;
    private String uname;
    private String gender;
    private boolean isAdmin;
    private Date dob;
    public User() {
    }

    public User(int id, String pass, String name, String uname, String gender, boolean isAdmin, Date dob) {
        this.id = id;
        this.pass = pass;
        this.name = name;
        this.uname = uname;
        this.gender = gender;
        this.isAdmin = isAdmin;
        this.dob = dob;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getPass() {
        return pass;
    }

    public void setPass(String pass) {
        this.pass = pass;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUname() {
        return uname;
    }

    public void setUname(String uname) {
        this.uname = uname;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public boolean isIsAdmin() {
        return isAdmin;
    }

    public void setIsAdmin(boolean isAdmin) {
        this.isAdmin = isAdmin;
    }

    public Date getDob() {
        return dob;
    }

    public void setDob(Date dob) {
        this.dob = dob;
    }

    @Override
    public String toString() {
        return uname;
    }
    
    

    
}
